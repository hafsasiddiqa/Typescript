// Array: An array is a special type of data type which can store multiple values of different data types sequentially using a special syntax.
// two ways to declare;
// way 1:
let pet = ["Cat", "Dog"];
console.log(pet[1]); // dog
// way 2: through angle-brackets.
let k = [1, 2, 3, 4];
console.log(k);
// declare empty Array:
let Pet = [];
console.log(Pet); // []
// dynamically adding array:
let P = [];
P.push("abc");
console.log(P); // [ 'abc' ]
